
# Получаем число
a = int(input('Введите проверяемое число:'))
b = 0
print("Вы ввели: ", a)

# Циклично перепроверяем делимость числа на другие числа до совпадения
for i in range (2, a // 2+1):
    if (a % i == 0):
        b = b + 1
if b <= 0:
    print("число является простым")
else:
    print("число не является простым")
